/* ================= データの引き継ぎ ================= */
function Backup({ state, update }) {
  const [open, setOpen] = useState(false);
  const [expText, setExpText] = useState("");
  const [impText, setImpText] = useState("");
  const [msg, setMsg] = useState("");

  const doExport = () => {
    setExpText(JSON.stringify(state));
    setMsg("下の文字列を全選択してコピーし、メモ帳などに保存してください。");
  };

  const doImport = () => {
    try {
      const parsed = JSON.parse(impText.trim());
      if (typeof parsed !== "object" || parsed === null || !("stamps" in parsed || "quiz" in parsed || "cards" in parsed)) {
        setMsg("このデータは形式が違うようです。エクスポートした文字列を丸ごと貼り付けてください。");
        return;
      }
      update({ ...DEFAULT_STATE, ...parsed });
      setImpText("");
      setMsg("インポートしました!押印・成績・カードの記録が復元されています。");
    } catch (e) {
      setMsg("読み込めませんでした。文字列が途中で切れていないか確認してください。");
    }
  };

  return (
    <section className="card">
      <button className="pm-head" style={{ padding: 0 }} onClick={() => setOpen(!open)}>
        <span className="label">データの引き継ぎ(バックアップ)</span>
        <span className="pm-arrow">{open ? "−" : "+"}</span>
      </button>
      {open && (
        <div style={{ marginTop: 10 }}>
          <p className="note" style={{ marginTop: 0 }}>
            アプリが新しいバージョンに更新されると、保存データは自動では引き継がれません。移行するときは、<strong>旧バージョンでエクスポート→新バージョンでインポート</strong>してください。定期的なバックアップにも使えます。
          </p>
          <button className="btn-sub" style={{ marginTop: 10 }} onClick={doExport}>エクスポート(文字列を表示)</button>
          {expText && (
            <textarea className="backup-ta" readOnly value={expText} onFocus={(e) => e.target.select()} />
          )}
          <div className="label" style={{ marginTop: 16 }}>インポート</div>
          <textarea
            className="backup-ta"
            placeholder="エクスポートした文字列をここに貼り付け"
            value={impText}
            onChange={(e) => setImpText(e.target.value)}
          />
          <button className="btn-main" style={{ marginTop: 8 }} onClick={doImport} disabled={!impText.trim()}>この内容で復元する</button>
          {msg && <p className="note" style={{ marginTop: 10 }}>{msg}</p>}
        </div>
      )}
    </section>
  );
}

