  const saveTimer = useRef(null);
  const stateRef = useRef(DEFAULT_STATE);

  /* ---- 読み込み(3回までリトライ) ---- */
  useEffect(() => {
    (async () => {
      for (let i = 0; i < 3; i++) {
        try {
          if (!window.storage) throw new Error("storage unavailable");
          const r = await window.storage.get(STORAGE_KEY);
          if (r && r.value) {
            const parsed = JSON.parse(r.value);
            setState({ ...DEFAULT_STATE, ...parsed });
          }
          setLoaded(true);
          return;
        } catch (e) {
          const notFound = String(e && e.message || e).toLowerCase();
          if (notFound.includes("not found") || notFound.includes("404")) {
            /* 初回起動: キーが無いだけなので正常 */
            setLoaded(true);
            return;
          }
          await new Promise((r2) => setTimeout(r2, 300 * (i + 1)));
        }
      }
      /* 読み込みに失敗しても操作は可能にする(保存側で警告を出す) */
      setLoaded(true);
      setStorageWarn(true);
    })();
  }, []);

  /* ---- 保存の実体(リトライ+読み戻し検証) ---- */
  const persist = async (showStatus = true) => {
    const payload = JSON.stringify(stateRef.current);
    for (let i = 0; i < 3; i++) {
      try {
        if (!window.storage) throw new Error("storage unavailable");
        const res = await window.storage.set(STORAGE_KEY, payload);
        if (res) {
          /* 読み戻して本当に書けたか検証 */
          const check = await window.storage.get(STORAGE_KEY);
          if (check && check.value === payload) {
            setStorageWarn(false);
            if (showStatus) {
              const d = new Date();
              setLastSaved(`${String(d.getHours()).padStart(2, "0")}:${String(d.getMinutes()).padStart(2, "0")}`);
              setSaveStatus("保存済み");
              setTimeout(() => setSaveStatus(""), 1500);
            }
            return true;
          }
        }
      } catch (e) { /* リトライへ */ }
      await new Promise((r) => setTimeout(r, 300 * (i + 1)));
    }
    setStorageWarn(true);
    setSaveStatus("");
    return false;
  };

  /* ---- 変更を検知して保存(デバウンス短縮) ---- */
  useEffect(() => {
    stateRef.current = state;
    if (!loaded) return;
    if (saveTimer.current) clearTimeout(saveTimer.current);
    saveTimer.current = setTimeout(() => { persist(); }, 400);
    return () => saveTimer.current && clearTimeout(saveTimer.current);
  }, [state, loaded]);

