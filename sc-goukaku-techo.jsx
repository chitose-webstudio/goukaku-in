function Quiz({ state, setState, stats }) {
  const [mode, setMode] = useState("menu"); // menu | run | result
  const [exam, setExam] = useState("A2"); // A2 | A1
  const [cat, setCat] = useState("全分野");
  const [ord, setOrd] = useState("weak"); // weak | random
  const [qs, setQs] = useState([]);
  const [idx, setIdx] = useState(0);
  const [picked, setPicked] = useState(null);
  const [session, setSession] = useState({ c: 0, t: 0 });

  const QLIST = exam === "A2" ? QUESTIONS : A1_QUESTIONS;
  const CLIST = exam === "A2" ? CATS : A1_CATS;
  const switchExam = (e) => { setExam(e); setCat("全分野"); };

  const start = () => {
    let pool = cat === "全分野" ? QLIST : QLIST.filter((q) => q.cat === cat);
    pool = shuffle(pool);
    if (ord === "weak") {
      // 未挑戦・誤答の多い問題を優先
      pool = pool.sort((a, b) => {
        const ra = state.quiz[a.id], rb = state.quiz[b.id];
        const sa = ra ? ra.c / ra.t : -1;
        const sb = rb ? rb.c / rb.t : -1;
        return sa - sb;
      });
    }
    setQs(pool.slice(0, 10).map((q) => ({ ...q, order: shuffle([0, 1, 2, 3]) })));
    setIdx(0); setPicked(null); setSession({ c: 0, t: 0 });
    setMode("run");
  };

  const answer = (origIdx) => {
    if (picked !== null) return;
    const q = qs[idx];
    const ok = origIdx === q.a;
    setPicked(origIdx);
    setSession((s) => ({ c: s.c + (ok ? 1 : 0), t: s.t + 1 }));
    setState((s) => {
      const prev = s.quiz[q.id] || { c: 0, t: 0 };
      return { ...s, quiz: { ...s.quiz, [q.id]: { c: prev.c + (ok ? 1 : 0), t: prev.t + 1 } } };
    });
  };

  const next = () => {
    if (idx + 1 >= qs.length) setMode("result");
    else { setIdx(idx + 1); setPicked(null); }
  };

  if (mode === "menu") {
    return (
      <div>
        <section className="card">
          <div className="label">一問一答</div>
          <div className="chips" style={{ marginBottom: 4 }}>
            <button className={"chip" + (exam === "A1" ? " on" : "")} onClick={() => switchExam("A1")}>科目A-1(応用情報レベル)</button>
            <button className={"chip" + (exam === "A2" ? " on" : "")} onClick={() => switchExam("A2")}>科目A-2(セキュリティ)</button>
          </div>
          <p className="note" style={{ marginTop: 4 }}>
            1セット最大10問(分野の問題数が少ない場合はその数だけ出ます)。「弱点優先」は間違えた問題・未挑戦の問題から、「ランダム」は完全シャッフルで出題します。
          </p>
          <div className="chips">
            {["全分野", ...CLIST].map((c) => (
              <button key={c} className={"chip" + (cat === c ? " on" : "")} onClick={() => setCat(c)}>{c}</button>
            ))}
          </div>
          <div className="label" style={{ marginTop: 4 }}>出題順</div>
          <div className="chips" style={{ marginTop: 8 }}>
            <button className={"chip" + (ord === "weak" ? " on" : "")} onClick={() => setOrd("weak")}>弱点優先</button>
            <button className={"chip" + (ord === "random" ? " on" : "")} onClick={() => setOrd("random")}>ランダム</button>
          </div>
          <button className="btn-main" onClick={start}>演習スタート({Math.min(10, (cat === "全分野" ? QLIST : QLIST.filter((q) => q.cat === cat)).length)}問)</button>
        </section>
        <section className="card">
          <div className="label">分野別成績({exam === "A2" ? "科目A-2" : "科目A-1"})</div>
          {CLIST.map((c) => {
            const r = (exam === "A2" ? stats.byCat : stats.a1.byCat)[c];
            const pct = r && r.t > 0 ? Math.round((r.c / r.t) * 100) : null;
            return (
              <div className="meter" key={c}>
                <div className="meter-head">
                  <span>{c}</span>
                  <span className="mono">{pct === null ? "未挑戦" : `${pct}%(${r.c}/${r.t})`}</span>
                </div>
                <div className="bar"><div className={"bar-fill" + (pct !== null && pct < 60 ? " low" : "")} style={{ width: `${pct || 0}%` }} /></div>
              </div>
            );
          })}
        </section>
      </div>
    );
  }

  if (mode === "result") {
    const pct = Math.round((session.c / session.t) * 100);
    return (
      <section className="card center">
        <div className="label">結果</div>
        <div className="big-num">{session.c}<span className="big-unit">/ {session.t}問</span></div>
        <p className="note">
          {pct >= 80 ? "8割超え。この分野は仕上がってきています。" :
           pct >= 60 ? "合格ライン(60%)は超えました。解説を読んだ問題が次の伸びしろです。" :
           "ここは伸びしろが大きい分野。解説の理解→数日後にもう一周、が最短ルートです。"}
        </p>
        <button className="btn-main" onClick={() => setMode("menu")}>メニューに戻る</button>
      </section>
    );
  }

  const q = qs[idx];
  return (
    <section className="card">
      <div className="quiz-head">
        <span className="label">{q.cat}</span>
        <span className="mono">{idx + 1} / {qs.length}</span>
      </div>
      <p className="q-text">{q.q}</p>
      <div className="choices">
        {q.order.map((oi) => {
          let cls = "choice";
          if (picked !== null) {
            if (oi === q.a) cls += " correct";
            else if (oi === picked) cls += " wrong";
            else cls += " dim";
          }
          return (
            <button key={oi} className={cls} onClick={() => answer(oi)}>
              {q.ch[oi]}
            </button>
          );
        })}
      </div>
      {picked === null && (
        <button className="btn-skip" onClick={() => answer(-1)}>わからない(正解を見る)</button>
      )}
      {picked !== null && (
        <div className="expl">
          <div className={"expl-head " + (picked === q.a ? "ok" : "ng")}>
            {picked === q.a ? "正解" : picked === -1 ? "正解はこちら" : "不正解"}
          </div>
          <p>{q.ex}</p>
          <button className="btn-main" onClick={next}>
            {idx + 1 >= qs.length ? "結果を見る" : "次の問題へ"}
          </button>
        </div>
      )}
    </section>
  );
}

/* ================= 用語カード ================= */
function Cards({ state, setState }) {
  const [deckKind, setDeckKind] = useState("SC"); // SC | BASIC
  const [cat, setCat] = useState("全分野");
  const [onlyTodo, setOnlyTodo] = useState(true);
  const [randomOrder, setRandomOrder] = useState(false);
  const [query, setQuery] = useState("");
  const [deck, setDeck] = useState(() => CARDS.filter((c) => true));
  const [i, setI] = useState(0);
  const [flip, setFlip] = useState(false);

  const SRC = deckKind === "SC" ? CARDS : BASIC_CARDS;
  const CL = deckKind === "SC" ? CATS : BASIC_CATS;

  // 条件変更時にデッキを組み直す(学習マーク中は組み直さず順序を保つ)
  useEffect(() => {
    let p = cat === "全分野" ? SRC : SRC.filter((c) => c.cat === cat);
    if (onlyTodo) p = p.filter((c) => state.cards[c.id] !== 1);
    if (randomOrder) p = shuffle(p);
    setDeck(p);
    setI(0);
    setFlip(false);
  }, [deckKind, cat, onlyTodo, randomOrder]); // state.cardsは意図的に依存から外している

  const switchDeck = (k) => { setDeckKind(k); setCat("全分野"); };
  const card = deck[i];

  const mark = (learnedFlag) => {
    if (!card) return;
    setState((s) => ({ ...s, cards: { ...s.cards, [card.id]: learnedFlag ? 1 : 0 } }));
    setFlip(false);
    if (learnedFlag && onlyTodo) {
      const nd = deck.filter((x) => x.id !== card.id);
      setDeck(nd);
      setI(nd.length === 0 ? 0 : i % nd.length);
    } else {
      setI(deck.length === 0 ? 0 : (i + 1) % deck.length);
    }
  };

  const reshuffle = () => {
    setDeck(shuffle(deck));
    setI(0);
    setFlip(false);
  };

  // 横断検索(カード全デッキ+全問題)
  const q = query.trim().toLowerCase();
  const results = useMemo(() => {
    if (!q) return null;
    const cardHits = [...CARDS, ...BASIC_CARDS].filter(
      (c) => (c.t + c.d + c.cat).toLowerCase().includes(q)
    );
    const qHits = [...QUESTIONS, ...A1_QUESTIONS].filter(
      (x) => (x.q + x.ch.join("") + x.ex).toLowerCase().includes(q)
    );
    return { cardHits, qHits };
  }, [q]);

  const learnedHere = SRC.filter((c) => state.cards[c.id] === 1).length;

  return (
    <div>
      <section className="card">
        <div className="quiz-head">
          <span className="label">用語カード</span>
          <span className="mono">習得 {learnedHere}/{SRC.length}</span>
        </div>

        <input
          className="search-input"
          type="search"
          placeholder="用語・問題を検索(例: ハッシュ、DNS、稼働率)"
          value={query}
          onChange={(e) => setQuery(e.target.value)}
        />

        {results ? (
          <div className="search-results">
            <div className="label" style={{ marginTop: 4 }}>カード: {results.cardHits.length}件</div>
            {results.cardHits.length === 0 && <p className="note">該当するカードはありません。</p>}
            {results.cardHits.slice(0, 15).map((c) => (
              <div className="sr" key={c.id}>
                <div className="sr-term">{c.t}<span className="sr-cat">{c.cat}</span></div>
                <div className="sr-def">{c.d}</div>
              </div>
            ))}
            {results.cardHits.length > 15 && (
              <p className="note">ほか{results.cardHits.length - 15}件。キーワードを絞ると見つけやすくなります。</p>
            )}
            <div className="label" style={{ marginTop: 16 }}>問題: {results.qHits.length}件</div>
            {results.qHits.length === 0 && <p className="note">該当する問題はありません。</p>}
            {results.qHits.slice(0, 10).map((x) => (
              <div className="sr" key={x.id}>
                <div className="sr-def"><strong>Q.</strong> {x.q}</div>
                <div className="sr-ans"><strong>A.</strong> {x.ch[x.a]}</div>
                <div className="sr-ex">{x.ex}</div>
              </div>
            ))}
            {results.qHits.length > 10 && (
              <p className="note">ほか{results.qHits.length - 10}件。</p>
            )}
          </div>
        ) : (
          <>
            <div className="chips" style={{ marginBottom: 4 }}>
              <button className={"chip" + (deckKind === "SC" ? " on" : "")} onClick={() => switchDeck("SC")}>SC用語(支援士)</button>
              <button className={"chip" + (deckKind === "BASIC" ? " on" : "")} onClick={() => switchDeck("BASIC")}>基礎(FE/AP)</button>
            </div>
            <div className="chips">
              {["全分野", ...CL].map((c) => (
                <button key={c} className={"chip" + (cat === c ? " on" : "")} onClick={() => setCat(c)}>{c}</button>
              ))}
            </div>
            <label className="toggle-row">
              <input type="checkbox" checked={onlyTodo} onChange={(e) => setOnlyTodo(e.target.checked)} />
              未習得のカードだけ表示
            </label>
            <label className="toggle-row">
              <input type="checkbox" checked={randomOrder} onChange={(e) => setRandomOrder(e.target.checked)} />
              ランダム順で表示
              {randomOrder && deck.length > 1 && (
                <button className="btn-mini" onClick={(e) => { e.preventDefault(); reshuffle(); }}>並びを変える</button>
              )}
            </label>

            {deck.length === 0 || !card ? (
              <div className="empty">
                この条件のカードは全部「覚えた」になっています。<br />
                チェックを外すと復習モードで全カードを見返せます。
              </div>
            ) : (
              <>
                <button className={"flashcard" + (flip ? " flipped" : "")} onClick={() => setFlip(!flip)}>
                  <div className="fc-cat">{card.cat} ・ {i + 1}/{deck.length}</div>
                  {!flip ? (
                    <>
                      <div className="fc-term">{card.t}</div>
                      <div className="fc-hint">タップして意味を表示</div>
                    </>
                  ) : (
                    <div className="fc-def">{card.d}</div>
                  )}
                </button>
                <div className="fc-btns">
                  <button className="btn-ng" onClick={() => mark(false)}>まだ</button>
                  <button className="btn-ok" onClick={() => mark(true)}>覚えた</button>
                </div>
              </>
            )}
          </>
        )}
      </section>
    </div>
  );
}

